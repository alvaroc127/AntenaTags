public class Bicicleta {
    
    int codChasis;
    String color;
    String Marca;

    public Bicicleta() {
    }

    
    
    public Bicicleta(int codChasis, String color, String marca) {
        
    }
    
    
    

    public int getCodChasis() {
        return codChasis;
    }

    public void setCodChasis(int codChasis) {
        this.codChasis = codChasis;
    }

    public String getColor() {
        return color;
    }

    public void setColor(String color) {
        this.color = color;
    }

    public String getMarca() {
        return Marca;
    }

    public void setMarca(String Marca) {
        this.Marca = Marca;
    }
    
    
    
    
    
    
}
